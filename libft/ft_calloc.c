#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	total;

	total = count * size;
	ptr = malloc(count * size);
	if (ptr == NULL)
		return (0);
	else
	{
		ft_bzero(ptr, total);
	}
	return (ptr);
}
